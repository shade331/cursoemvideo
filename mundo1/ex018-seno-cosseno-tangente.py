import math
a = float(input('Digite o ângulo que você deseja: '))
print(f'O ângulo de {a:.1f} tem o SENO de {math.sin(math.radians(a)):.2f}')
print(f'O ângulo de {a:.1f} tem o COSSENO de {math.cos(math.radians(a)):.2f}')
print(f'O ângulo de {a:.1f} tem a TANGENTE de {math.tan(math.radians(a)):.2f}')
